const sortedPhraseTranslations = [...enPhraseTranslations].sort(
  ([left], [right]) => right.length - left.length,
);

function interpolate(text: string, params?: TranslationParams): string {
  if (!params) {
    return text;
  }

  return text.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = params[key];
    return value === undefined || value === null ? match : String(value);
  });
}

function preserveOuterWhitespace(source: string, translated: string): string {
  const match = source.match(/^(\s*)([\s\S]*?)(\s*)$/);
  if (!match) {
    return translated;
  }

  return `${match[1]}${translated}${match[3]}`;
}

function applyPhraseTranslations(source: string): string {
  let result = source;

  sortedPhraseTranslations.forEach(([zh, en]) => {
    result = result.split(zh).join(en);
  });

  return result
    .replace(/(\d+)\s*s/g, '$1s')
    .replace(/(\d+)\s*min/g, '$1 min')
    .replace(/(\d+)\s*h/g, '$1h')
    .replace(/(\d+)\s*d/g, '$1d')
    .replace(/：/g, ': ')
    .replace(/，/g, ', ')
    .replace(/。/g, '.')
    .replace(/？/g, '?')
    .replace(/！/g, '!')
    .replace(/、/g, ', ')
    .replace(/“|”/g, '"')
    .replace(/\s+\./g, '.')
    .replace(/\s+,/g, ',')
    .replace(/\s+:/g, ':')
    .replace(/\s{2,}/g, ' ')
    .trim();
}

export function hasTranslatableContent(text: string): boolean {
  return /[\u3400-\u9fff]/.test(text) || !!enExactTranslations[text.trim()];
}

export function translateText(
  source: string,
  language: Language,
  params?: TranslationParams,
): string {
  const interpolatedSource = interpolate(source, params);

  if (language === 'zh') {
    return interpolatedSource;
  }

  const trimmed = interpolatedSource.trim();
  const exact = enExactTranslations[trimmed];
  const translated = exact ?? applyPhraseTranslations(trimmed);

  return preserveOuterWhitespace(interpolatedSource, translated);
}
